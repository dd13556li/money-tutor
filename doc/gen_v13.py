# ── 低階工具 ────────────────────────────────────────────
def set_font(run, size=12, bold=False, color=None, name='標楷體'):
    run.font.name = name
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.color.rgb = RGBColor(*(color or (0, 0, 0)))
    try:
        rPr = run._element.get_or_add_rPr()
        rPr.get_or_add_rFonts().set(qn('w:eastAsia'), name)
    except Exception:
        pass

def pf(para, before=0, after=4, line=1.5,
       align=WD_ALIGN_PARAGRAPH.JUSTIFY, indent=0, first=0):
    f = para.paragraph_format
    f.space_before = Pt(before); f.space_after = Pt(after)
    if line:
        f.line_spacing_rule = WD_LINE_SPACING.MULTIPLE
        f.line_spacing = line
    f.alignment = align
    if indent: f.left_indent  = Cm(indent)
    if first:  f.first_line_indent = Cm(first)

def shade_tc(tc, fill):
    tcPr = tc.get_or_add_tcPr()
    shd  = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear'); shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), fill); tcPr.append(shd)

def shade_p(para, fill):
    pPr = para._p.get_or_add_pPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear'); shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), fill); pPr.append(shd)

def inline(para, text, size=12, bold=False, color=None, name='標楷體'):
    for part in re.split(r'(\*\*[^*]+\*\*)', text):
        if part.startswith('**') and part.endswith('**'):
            r = para.add_run(part[2:-2]); set_font(r, size, True, color, name)
        else:
            r = para.add_run(part);       set_font(r, size, bold, color, name)

def div(doc, color='2E7D32', thick=6):
    p = doc.add_paragraph(); pf(p, 3, 3, None)
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    b = OxmlElement('w:bottom')
    b.set(qn('w:val'), 'single'); b.set(qn('w:sz'), str(thick))
    b.set(qn('w:space'), '1');    b.set(qn('w:color'), color)
    pBdr.append(b); pPr.append(pBdr)

def pgbr(doc):
    p = doc.add_paragraph(); pf(p, 0, 0, None)
    r = p.add_run()
    br = OxmlElement('w:br'); br.set(qn('w:type'), 'page')
    r._r.append(br)

def P(doc, text='', size=12, bold=False, color=None,
      before=0, after=4, line=1.5,
      align=WD_ALIGN_PARAGRAPH.JUSTIFY, indent=0,
      bullet=None, italic=False):
    para = doc.add_paragraph()
    ind  = indent
    if bullet:
        ind  = indent + 0.55; text = bullet + '\u3000' + text
        para.paragraph_format.first_line_indent = Cm(-0.55)
    pf(para, before, after, line, align, ind)
    inline(para, text, size, bold, color)
    if italic:
        for r in para.runs: r.font.italic = True
    return para

def BOX(doc, text, fill=BG1, size=11, bold=False, color=None, before=4, after=6):
    para = doc.add_paragraph()
    pf(para, before, after, 1.4, WD_ALIGN_PARAGRAPH.JUSTIFY, 0.4)
    para.paragraph_format.right_indent = Cm(0.4)
    shade_p(para, fill)
    inline(para, text, size, bold, color)
    return para

def H(doc, text, level=1):
    sz = {1: 18, 2: 14, 3: 12, 4: 12}[level]
    co = {1: C1, 2: C4, 3: C3, 4: (0, 0, 100)}[level]
    al = WD_ALIGN_PARAGRAPH.CENTER if level == 1 else WD_ALIGN_PARAGRAPH.LEFT
    bf = {1: 12, 2: 10, 3: 6, 4: 4}[level]
    p  = doc.add_paragraph(); r = p.add_run(text)
    set_font(r, sz, True, co)
    pf(p, bf, 4, 1.2, al)
    return p

def TBL(doc, headers, rows, cw=None,
        hfill='2E7D32', hco=(255, 255, 255), hs=10.5, ds=10.5):
    t = doc.add_table(1 + len(rows), len(headers))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    t.style = 'Table Grid'
    for i, h in enumerate(headers):
        cell = t.rows[0].cells[i]
        shade_tc(cell._tc, hfill)
        cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
        p = cell.paragraphs[0]
        pf(p, 2, 2, 1.2, WD_ALIGN_PARAGRAPH.CENTER)
        inline(p, h, hs, True, hco)
    for ri, row in enumerate(rows):
        fill = 'F1F8E9' if ri % 2 == 0 else 'FFFFFF'
        for ci, ct in enumerate(row):
            cell = t.rows[ri + 1].cells[ci]
            shade_tc(cell._tc, fill)
            cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
            p = cell.paragraphs[0]
            pf(p, 2, 2, 1.3, WD_ALIGN_PARAGRAPH.LEFT)
            inline(p, str(ct), ds)
    if cw:
        for i, w in enumerate(cw):
            for row in t.rows: row.cells[i].width = Cm(w)
    doc.add_paragraph()
    return t

def new_doc():
    doc = Document()
    sec = doc.sections[0]
    sec.page_width  = Cm(21); sec.page_height = Cm(29.7)
    sec.left_margin = sec.right_margin  = Cm(2.54)
    sec.top_margin  = sec.bottom_margin = Cm(2.54)
    sty = doc.styles['Normal']
    sty.font.name = '標楷體'; sty.font.size = Pt(12)
    try: sty.element.rPr.rFonts.set(qn('w:eastAsia'), '標楷體')
    except: pass
    return doc

