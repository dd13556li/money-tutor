def border_table(table, color="2E5BA6", sz="8"):
    tbl = table._tbl
    tblPr = tbl.tblPr
    tblBorders = OxmlElement('w:tblBorders')
    for bn in ['top','left','bottom','right','insideH','insideV']:
        b = OxmlElement(f'w:{bn}')
        b.set(qn('w:val'), 'single')
        b.set(qn('w:sz'), sz)
        b.set(qn('w:space'), '0')
        b.set(qn('w:color'), color)
        tblBorders.append(b)
    tblPr.append(tblBorders)

def cell_bg(cell, hex_c):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_c)
    tcPr.append(shd)

def cell_set(cell, text_or_lines, size=10, bold=False,
             align=WD_ALIGN_PARAGRAPH.LEFT, ls=1.25,
             fg=None, bg=None):
    """填入儲存格（字串或 list）"""
    cell.text = ""
    if bg:
        cell_bg(cell, bg)
    lines = [text_or_lines] if isinstance(text_or_lines, str) else text_or_lines
    first = True
    for line in lines:
        para = cell.paragraphs[0] if first else cell.add_paragraph()
        first = False
        if line == "":
            set_para_fmt(para, align=align, sb=0, sa=0, ls=ls)
            continue
        is_hdr = (line.startswith("【") and line.endswith("】")) or line.startswith("▌")
        r = para.add_run(line)
        set_font(r, size=size, bold=(bold or is_hdr),
                 color=fg if fg else None)
        set_para_fmt(para, align=align, sb=1, sa=1, ls=ls)
    if fg:
        for para in cell.paragraphs:
            for r in para.runs:
                r.font.color.rgb = RGBColor(*fg)

def col_w(table, widths_cm):
    for row in table.rows:
        for j, w in enumerate(widths_cm):
            if j < len(row.cells):
                row.cells[j].width = Cm(w)

def make_tbl(doc, headers, rows, widths,
             hdr_bg="1B3A6B", alt_bg="EDF2FB", data_size=10):
    t = doc.add_table(rows=len(rows)+1, cols=len(headers))
    border_table(t)
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    # header
    for j, h in enumerate(headers):
        c = t.rows[0].cells[j]
        cell_set(c, h, size=11, bold=True,
                 align=WD_ALIGN_PARAGRAPH.CENTER, bg=hdr_bg, fg=(255,255,255))
    # data
    for i, row in enumerate(rows):
        bg = alt_bg if i % 2 == 1 else None
        for j, cell_val in enumerate(row):
            c = t.rows[i+1].cells[j]
            if isinstance(cell_val, list):
                cell_set(c, cell_val, size=data_size, bg=bg)
            else:
                cell_set(c, str(cell_val), size=data_size, bg=bg)
    col_w(t, widths)
    return t

def info_tbl(doc, data, widths=(4.0, 12.5)):
    """雙欄說明表（左欄深藍標籤，右欄內容）"""
    t = doc.add_table(rows=len(data), cols=2)
    border_table(t)
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, (label, val) in enumerate(data):
        lc, rc = t.rows[i].cells[0], t.rows[i].cells[1]
        cell_set(lc, label, size=11, bold=True,
                 align=WD_ALIGN_PARAGRAPH.CENTER, bg="1B3A6B", fg=(255,255,255))
        lc.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
        if isinstance(val, list):
            cell_set(rc, val, size=11)
        else:
            cell_set(rc, str(val), size=11)
    col_w(t, widths)
    return t

