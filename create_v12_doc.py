def set_font(run, size=12, bold=False, italic=False,
             zh_font="標楷體", en_font="Times New Roman", color=None):
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.italic = italic
    run.font.name = en_font
    run._element.rPr.rFonts.set(qn('w:eastAsia'), zh_font)
    if color:
        run.font.color.rgb = RGBColor(*color)

def set_para_fmt(para, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
                 sb=0, sa=4, ls=1.5, indent_cm=0.0):
    pf = para.paragraph_format
    pf.alignment = align
    pf.space_before = Pt(sb)
    pf.space_after = Pt(sa)
    pf.line_spacing_rule = WD_LINE_SPACING.MULTIPLE
    pf.line_spacing = ls
    if indent_cm > 0:
        pf.first_line_indent = Cm(indent_cm)

def add_p(doc, text, size=11, bold=False, italic=False,
          align=WD_ALIGN_PARAGRAPH.JUSTIFY, indent=False,
          sb=0, sa=4, ls=1.5, color=None):
    p = doc.add_paragraph()
    r = p.add_run(text)
    set_font(r, size=size, bold=bold, italic=italic, color=color)
    set_para_fmt(p, align=align, sb=sb, sa=sa, ls=ls,
                 indent_cm=0.85 if indent else 0.0)
    return p

