def set_font(run, size=12, bold=False, zh_font="標楷體", en_font="Times New Roman", color=None):
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.name = en_font
    run._element.rPr.rFonts.set(qn('w:eastAsia'), zh_font)
    if color:
        run.font.color.rgb = RGBColor(*color)

def set_para_format(para, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
                    space_before=0, space_after=6, line_spacing=1.5):
    pf = para.paragraph_format
    pf.alignment = align
    pf.space_before = Pt(space_before)
    pf.space_after = Pt(space_after)
    pf.line_spacing_rule = WD_LINE_SPACING.MULTIPLE
    pf.line_spacing = line_spacing

def add_heading(doc, text, level=1, size=14, center=True):
    para = doc.add_paragraph()
    run = para.add_run(text)
    set_font(run, size=size, bold=True)
    align = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.LEFT
    set_para_format(para, align=align, space_before=8, space_after=8)
    return para

def add_para(doc, text, size=12, bold=False, indent=False, align=WD_ALIGN_PARAGRAPH.JUSTIFY, color=None):
    para = doc.add_paragraph()
    run = para.add_run(text)
    set_font(run, size=size, bold=bold, color=color)
    set_para_format(para, align=align)
    if indent:
        para.paragraph_format.first_line_indent = Cm(0.85)
    return para

def set_cell_text(cell, text, size=11, bold=False, align=WD_ALIGN_PARAGRAPH.LEFT,
                  space_before=2, space_after=2, line_spacing=1.3):
    cell.text = ""
    para = cell.paragraphs[0]
    run = para.add_run(text)
    set_font(run, size=size, bold=bold)
    set_para_format(para, align=align, space_before=space_before,
                    space_after=space_after, line_spacing=line_spacing)

def set_cell_multiline(cell, lines, size=11, line_spacing=1.3):
    """多行文字填入儲存格"""
    cell.text = ""
    first = True
    for line in lines:
        if first:
            para = cell.paragraphs[0]
            first = False
        else:
            para = cell.add_paragraph()
        is_header = line.startswith("【") and line.endswith("】")
        is_sub = line.startswith("▶") or line.startswith("●") or line.startswith("◆")
        run = para.add_run(line)
        set_font(run, size=size, bold=(is_header or is_sub))
        set_para_format(para, space_before=1, space_after=1, line_spacing=line_spacing)

def set_cell_bg(cell, hex_color="D9E1F2"):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color)
    tcPr.append(shd)

def set_table_border(table):
    tbl = table._tbl
    tblPr = tbl.tblPr
    tblBorders = OxmlElement('w:tblBorders')
    for border_name in ['top', 'left', 'bottom', 'right', 'insideH', 'insideV']:
        border = OxmlElement(f'w:{border_name}')
        border.set(qn('w:val'), 'single')
        border.set(qn('w:sz'), '6')
        border.set(qn('w:space'), '0')
        border.set(qn('w:color'), '4472C4')
        tblBorders.append(border)
    tblPr.append(tblBorders)

def set_col_widths(table, widths_cm):
    for row in table.rows:
        for j, w in enumerate(widths_cm):
            if j < len(row.cells):
                row.cells[j].width = Cm(w)

def make_table(doc, headers, rows, col_widths, header_bg="1F3864", header_color=(255,255,255),
               alt_row_bg="EEF3FC"):
    t = doc.add_table(rows=len(rows)+1, cols=len(headers))
    set_table_border(t)
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    # 標題列
    for j, h in enumerate(headers):
        c = t.rows[0].cells[j]
        set_cell_text(c, h, size=11, bold=True, align=WD_ALIGN_PARAGRAPH.CENTER)
        set_cell_bg(c, header_bg)
        # 設定白色文字
        for para in c.paragraphs:
            for run in para.runs:
                run.font.color.rgb = RGBColor(*header_color)
    # 資料列
    for i, row_data in enumerate(rows):
        for j, text in enumerate(row_data):
            c = t.rows[i+1].cells[j]
            if isinstance(text, list):
                set_cell_multiline(c, text, size=10)
            else:
                set_cell_text(c, str(text), size=10)
            if i % 2 == 1:
                set_cell_bg(c, alt_row_bg)
    set_col_widths(t, col_widths)
    return t

def fill_info_row(table, row_idx, label, value, label_bg="1F3864", value_multiline=False):
    row = table.rows[row_idx]
    lc, rc = row.cells[0], row.cells[1]
    set_cell_text(lc, label, size=11, bold=True, align=WD_ALIGN_PARAGRAPH.CENTER)
    set_cell_bg(lc, label_bg)
    for para in lc.paragraphs:
        for run in para.runs:
            run.font.color.rgb = RGBColor(255, 255, 255)
    lc.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
    if value_multiline and isinstance(value, list):
        set_cell_multiline(rc, value, size=11)
    else:
        set_cell_text(rc, str(value), size=11)

def make_info_table(doc, data, col_widths=(3.8, 12.7)):
    t = doc.add_table(rows=len(data), cols=2)
    set_table_border(t)
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, (label, value) in enumerate(data):
        fill_info_row(t, i, label, value,
                      value_multiline=isinstance(value, list))
    set_col_widths(t, col_widths)
    return t

